public class ToIsLessThanFromException extends Exception{
    public ToIsLessThanFromException() {
    }

    public ToIsLessThanFromException(String message) {
        super(message);
    }

    public ToIsLessThanFromException(String message, Throwable cause) {
        super(message, cause);
    }

    public ToIsLessThanFromException(Throwable cause) {
        super(cause);
    }

    public ToIsLessThanFromException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
